package Java_Final_3_2023;

public class Person {
    private String name;
    private String dateOfBirth;

    public void setFullName(String fullName) {
        name = fullName;
    }

    public String getFullName() {
        return name;
    }

    public void setDateOfBirth(String DOB) {
        dateOfBirth = DOB;
    }

    public String getDateOfBirth() {
        return dateOfBirth;
    }
 
}
